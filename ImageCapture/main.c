uint32_t readSize(void){
	uint32_t low = readSPI(0x42);
	uint32_t mid =  readSPI(0x43);
	uint32_t high = readSPI(0x44) & 0x7f;
	
	uint32_t size =  ((high << 16) | (mid << 8) | low) & 0x07fffff;

	
	return size;
}

void i2c_init(void){
	/* initialize TWI clock: 100 kHz clock, TWPS = 0 => prescaler = 1 */
	
	TWSR = 0;                         /* no prescaler */
	TWBR = ((4*F_CPU/SCL_CLOCK)-16)/2;  /* must be > 10 for stable operation */
	
}

unsigned char i2c_start(unsigned char address){
	uint8_t   twst;

	// send START condition
	TWCR = (1<<TWINT) | (1<<TWSTA) | (1<<TWEN);

	// wait until transmission completed
	while(!(TWCR & (1<<TWINT)));

	// check value of TWI Status Register. Mask prescaler bits.
	twst = TW_STATUS & 0xF8;
	if ( (twst != TW_START) && (twst != TW_REP_START)) return 1;

	// send device address
	TWDR = address;
	TWCR = (1<<TWINT) | (1<<TWEN);

	// wail until transmission completed and ACK/NACK has been received
	while(!(TWCR & (1<<TWINT)));

	// check value of TWI Status Register. Mask prescaler bits.
	twst = TW_STATUS & 0xF8;
	if ( (twst != TW_MT_SLA_ACK) && (twst != TW_MR_SLA_ACK) ) return 1;

	return 0;

	}
	/* i2c_start */

void i2c_stop(void){
	/* send stop condition */
	TWCR = (1<<TWINT) | (1<<TWEN) | (1<<TWSTO);
	
	// wait until stop condition is executed and bus released
	while(TWCR & (1<<TWSTO));

	}
	/* i2c_stop */


unsigned char i2c_write( unsigned char data ){
	uint8_t   twst;
	
	// send data to the previously addressed device
	TWDR = data; //for write only
	TWCR = (1<<TWINT) | (1<<TWEN);

	// wait until transmission completed
	while(!(TWCR & (1<<TWINT)));

	// check value of TWI Status Register. Mask prescaler bits
	twst = TW_STATUS & 0xF8;
	if( twst != TW_MT_DATA_ACK) return 1;
	return 0;

}
/* i2c_write */
	

unsigned char i2c_readNak(void)
{
	TWCR = (1<<TWINT) | (1<<TWEN);
	while(!(TWCR & (1<<TWINT)));
	
	return TWDR;

}
/* i2c_readNak */

void sensor_set(unsigned char reg1, unsigned char reg2, unsigned char data)
{
	i2c_start(0x78);
	
	i2c_write(reg1);
	i2c_write(reg2); //write to register 3103
	
	i2c_write(data); //write 0x03 to register 3103
}

void set_bit(uint8_t addr, uint8_t bit)
{
	uint8_t temp;
	temp = readSPI(addr);
	writeSPI(0x80|addr, temp | bit);
}

