void sd_write(void){

	// reboot delay
	_delay_ms(200);
	
	// init sdcard
	UINT bw;
	f_mount(0, &FatFs);		// Give a work area to the FatFs module
	// open file
	fp = (FIL *)malloc(sizeof (FIL));

	uint8_t buf[256];
	int m = 0;
	uint8_t first_frame = 1; 
	
	
	uint8_t temp=0;
	uint8_t temp_last=0;
	while(( temp != 0xD9) | (temp_last !=0xFF)) {
		temp_last =  temp;
		temp = readSPI(0x3D);
		buf[m++] = temp;
		_delay_us(15);
		
		if(m >= 256) {
			if (first_frame) {
				if (f_open(fp, "image.jpg", FA_WRITE | FA_CREATE_ALWAYS) == FR_OK) {	// Create a file
					f_write(fp, buf, sizeof(buf), &bw);	// Write data to the file
				}
				first_frame = 0;
				
			} else {
				if (f_lseek(fp, f_size(fp)) == FR_OK) {
					f_write(fp, buf, sizeof(buf), &bw);	// Write data to the file
				}
			}
			m = 0;
		}
	}
	
	 if(m > 0 ) {
		if (f_lseek(fp, f_size(fp)) == FR_OK) {
			f_write(fp, buf, sizeof(buf), &bw);	// Write data to the file
		}
	 }
	 
	 f_close(fp); // Close the file
}

