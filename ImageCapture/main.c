/* Used to provide a Timestamp for SDCard files and folders*/
DWORD get_fattime (void)
{
	// Returns current time packed into a DWORD variable
	return	  ((DWORD)(2013 - 1980) << 25)	// Year 2013
	| ((DWORD)8 << 21)				// Month 7
	| ((DWORD)2 << 16)				// Day 28
	| ((DWORD)20 << 11)				// Hour 0..24
	| ((DWORD)30 << 5)				// Min 0
	| ((DWORD)0 >> 1);				// Sec/2 0
}

